package com.kh.SetEx;

import java.util.TreeMap;

public class TreeMapPre {

	public static void main(String[] args) {
	/*
	 String,String 으로 
	 "apple" "사과"	
	 */
		
		TreeMap<String,String>t = new TreeMap<>();
		
		String[] key = {"apple","banana","orange","grape","watermelon"};
		String[] value = {"사과","바나나","오렌지","포도","멜론"};
		
		for(int i = 0; i<key.length; i++) {
			t.put(key[i], value[i]);
		}
		System.out.println(t);
		
		System.out.println(t.get("바나나"));
		
		System.out.println(t.size());
		
		System.out.println(t.isEmpty());
		
		System.out.println(t.containsKey("grape"));
		
		System.out.println(t.containsValue("포도"));
		
		System.out.println(t.keySet());
		
		System.out.println(t.values());
		
		System.out.println(t.firstKey());
		
		System.out.println(t.lastKey());
		
		t.clear();
		System.out.println(t);
	}
	
	
	
	
	
	
}
