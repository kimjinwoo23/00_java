package com.kh.variable.ex;

public class VariableEx1 {

	public static void main(String[] args) {
	//변수 : 메모리에 값을 저장하기 위한 공간
	//저장되는 값이 변할 수 있기 떄문에 변수 
	//자료형: 변수 또는 값의 크기나 형식을 결정 
	/*
	 * 자바에서 기본으로 제공하는 자료형
	 * 논리형
	 * boolean (1byte) -true/false
	 * 
	 * 정수형
	 * byte (1byte)
	 * short (2byte)
	 * int (4byte)
	 * long (8byte)
	 * 
	 * 실수형 
	 * float (4byte) -숫자뒤에 f,F가 들어가야함
	 * double(8byte)
	 * 
	 * 문자형 
	 * char (2byte)-문자 하나,양 옆에 ''사용
	 * 
	 * 참조형
	 * 자바에서 기본은 아니지만 기본이 된 문자열
	 * 
	 * 문자열 
	 * String 크기를 특정하게 지정할수없음 
	 * 표현 방법은 문자열 양 옆에 " " 사용
	 * 
	 *변수 선언 : 메모리에 공간을 할당
	 *값 대입 : 변수에 값을 집어넣는 것 
	 *변수를 선언할 떄 자료형은 변수명마다 최초 1회만 선언 
	 *
	 */
	//자료형 변수 선언   =   값 대입  ;(종료)
	int    number    =   1;
	//변수명에 들어있는 숫자를 변경할수있다  
	//변경가능할때 자료형은 사용하지 않음 
	number = 3;
	//number 안에는 1대신 3이 적용
	
	
	
	
	
	
	
	
	
	
	}

}
